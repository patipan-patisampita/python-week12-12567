#1.Arithemetic Operator
#=====================

a = 100
b = 50
#1.Addition:หาผลบวก
print("เรามีเงิน:",a + b,"บาท")

#2.Subtraction:หาผลลบ
print("เรามีเงิน:",a - b,"บาท")

#3.Multiplication:หาผลคูณ
print("เรามีเงิน:",a * b,"บาท")

#4.Division:หาผลหาร
print("เรามีเงิน:",a / b,"บาท")

#5.Modulus:หาผลหารเอาเศษที่เหลือ
print("เรามีเงิน:",a % b,"บาท")

#6.Powe:หาผลเลขยกกำลัง
print("เรามีเงิน:",a ** b,"บาท")

