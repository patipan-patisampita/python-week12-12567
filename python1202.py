#2.Assignment Operator
a = 10

#2.1 Assing value
b = a
print("Assing value:",b)

#2.2 Add and assign value
b += a #=>: b = b + a
print("Assing value:",b) #=>:20

#2.3 Subtract and assign value
b -= a #=>: b = b - a
print("Assing value:",b) #=>:10

#2.4 Multiply and assign value
b *= a #=>: b = b * a
print("Assing value:",b) #=>:100

#2.5 bitwise lishift operator
b <<= a #=>: b = b << a
print("Assing value:",b) #=>:?

